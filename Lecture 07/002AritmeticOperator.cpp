#include<iostream>
using namespace std;
int main(){
    int x = 10;
    int y = 20;
    // Arithmetic Operators: +, -, *, /, %
    //addition
    cout<<(x + y)<<endl;
    //subtraction
    cout<<(x - y)<<endl;
    //multiplication
    cout<<(x * y)<<endl;
    //division
    cout<<(x / y)<<endl;
    //modulus   
    cout<<(x % y)<<endl;
    //power
    // cout<<(x**y)<<endl; // this is not valid in c++
    return 0;
}